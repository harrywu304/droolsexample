public class ExamScore {
	
	private String stuId;
	
	private int yuwenScore;
	
	private int shuxueScore;
	
	private int yingyuScore;
	

	/**
	 * @return the stuId
	 */
	public String getStuId() {
		return stuId;
	}

	/**
	 * @param stuId the stuId to set
	 */
	public void setStuId(String stuId) {
		this.stuId = stuId;
	}

	/**
	 * @return the yuwenScore
	 */
	public int getYuwenScore() {
		return yuwenScore;
	}

	/**
	 * @param yuwenScore the yuwenScore to set
	 */
	public void setYuwenScore(int yuwenScore) {
		this.yuwenScore = yuwenScore;
	}

	/**
	 * @return the shuxueScore
	 */
	public int getShuxueScore() {
		return shuxueScore;
	}

	/**
	 * @param shuxueScore the shuxueScore to set
	 */
	public void setShuxueScore(int shuxueScore) {
		this.shuxueScore = shuxueScore;
	}

	/**
	 * @return the yingyuScore
	 */
	public int getYingyuScore() {
		return yingyuScore;
	}

	/**
	 * @param yingyuScore the yingyuScore to set
	 */
	public void setYingyuScore(int yingyuScore) {
		this.yingyuScore = yingyuScore;
	}

	
}
